    namespace detail
    {
        /// Apply a wrapper's quantize_for_diversity() if it exposes one;
        /// otherwise return the value unchanged.  This allows custom wrappers
        /// that pre-date this interface to continue working without modification.
        template<typename W>
        [[nodiscard]] auto quantize_gene(const W& wrapper,
            const typename W::value_type& v)
            -> typename W::value_type
        {
            if constexpr (requires { wrapper.quantize_for_diversity(v); })
                return wrapper.quantize_for_diversity(v);
            else
                return v;
        }

    }

    template<typename Container, GeneticWrapper ElementWrapper>
        requires std::is_arithmetic_v<typename ElementWrapper::value_type>
    && std::same_as<typename Container::value_type, typename ElementWrapper::value_type>
        class container_range   
    {
        using element_type = typename ElementWrapper::value_type;
        using value_type = Container;

        size_t         container_size;
        ElementWrapper element_wrapper;
        double         per_element_mut_prob;

    public:

        container_range(size_t container_size, ElementWrapper element_wrapper, double per_element_mut_prob = -1.0)
            : container_size(container_size), element_wrapper(std::move(element_wrapper)), per_element_mut_prob(per_element_mut_prob) {
        }
        
        auto get_container_size() const noexcept { return container_size; }

        template<typename RNG>
        [[nodiscard]] value_type random_value(RNG& rng) const {
            value_type result;
            if constexpr (requires(value_type v, size_t n) { v.resize(n); })
                result.resize(container_size);
            for (auto& el : result)
                el = element_wrapper.random_value(rng);
            return result;
        }

        template<typename RNG>
        [[nodiscard]] value_type mutate(value_type value, RNG& rng) const {
            double prob = (per_element_mut_prob >= 0.0)
                ? per_element_mut_prob
                : 1.0 / static_cast<double>(container_size);
            std::uniform_real_distribution<double> coin{ 0.0, 1.0 };
            for (auto& el : value)
                if (coin(rng) < prob)
                    el = element_wrapper.mutate(el, rng);
            return value;
        }

        /// Quantize each container element using the element wrapper's own
        /// diversity_epsilon (if it has one).
        [[nodiscard]] value_type quantize_for_diversity(value_type value) const {
            for (auto& el : value)
                el = detail::quantize_gene(element_wrapper, el);
            return value;
        }

        template<typename RNG>
        [[nodiscard]] value_type crossover(const value_type& a, const value_type& b,
            RNG& rng) const
        {
            value_type result;
            if constexpr (requires(value_type v, size_t n) { v.resize(n); })
                result.resize(container_size);

            if (container_size <= 1) {
                // Degenerate container: delegate to element-level crossover so
                // the single gene is properly recombined rather than cloned.
                if (container_size == 1)
                    *std::begin(result) =
                    element_wrapper.crossover(*std::begin(a), *std::begin(b), rng);
                return result;
            }

            // Restrict cut to [1, container_size-1] so the result is never a
            // verbatim clone of either parent.
            size_t cut = std::uniform_int_distribution<size_t>{ 1, container_size - 1 }(rng);
            auto ia = std::begin(a), ib = std::begin(b), ir = std::begin(result);
            for (size_t i = 0; i < container_size; ++i, ++ia, ++ib, ++ir)
                *ir = (i < cut) ? *ia : *ib;
            return result;
        }

        auto serialize(this auto&& self, auto&& archive)
        {
            std::invoke(std::forward<decltype(archive)>(archive),
                self.container_size,
                self.element_wrapper,
                self.per_element_mut_prob
            );
        }

    };


