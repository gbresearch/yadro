#pragma once
#include <thread>
#include <algorithm>
#include <limits>
#include <atomic>
#include <chrono>

// intrinsics for pause/yield
#if defined(__i386__) || defined(__x86_64__) || defined(_M_IX86) || defined(_M_X64)
#include <immintrin.h>
#define YADRO_SPIN_PAUSE() _mm_pause()
#elif defined(__aarch64__) || defined(__arm__)
#define YADRO_SPIN_PAUSE() __builtin_arm_yield()
#else
#define YADRO_SPIN_PAUSE() std::this_thread::yield()
#endif


namespace gb::yadro::util
{
    //-------------------------------------------------------------------------
    // spin and wait class using platform-specific pause/yield

    struct spin_wait
    {
        inline void yield() noexcept
        {
            if (spin_count < (pause_limit >> 2)) {
                // tight spin — best for short critical sections
#if defined(__GNUC__) || defined(__clang__)
                asm volatile("" ::: "memory"); // compiler barrier
#endif
            }
            else if (spin_count < pause_limit) {
                YADRO_SPIN_PAUSE();
            }
            else {
                std::this_thread::yield();
            }

            if (spin_count < std::numeric_limits<unsigned long long>::max())
                ++spin_count;
        }

        auto get_count() const noexcept { return spin_count; }

    private:
        unsigned long long spin_count{ num_cores > 1u ? 0u : pause_limit };
        static constexpr unsigned pause_limit = 32u;
        static inline const unsigned num_cores = std::max(1u, std::thread::hardware_concurrency());
    };

    /// This mutex initially spins for short durations when acquiring the lock,
    /// then wawits using atomic wait/notify once sustained contention
    /// is detected. This approach combines low-latency benefits of spin-locks
    /// for short critical sections with improved CPU efficiency under sustained
    /// contention compared to pure polling.
    /// 
    /// Compatible with std::lock_guard and std::unique_lock.

    class hybrid_mutex
    {
        static constexpr unsigned long long spin_threshold = 1000;
    public:
        hybrid_mutex() noexcept : state(0) {}
        hybrid_mutex(const hybrid_mutex&) = delete;
        hybrid_mutex(hybrid_mutex&&) = delete;
        hybrid_mutex& operator=(const hybrid_mutex&) = delete;
        hybrid_mutex& operator=(hybrid_mutex&&) = delete;

        // -------------------------------------------------------------------------
        // Lock / Try lock / Unlock
        // -------------------------------------------------------------------------
        [[nodiscard]] void lock() noexcept
        {
            // Phase 1: short spin
            for(spin_wait spinner; spinner.get_count() < spin_threshold; )
            {
                if (auto expected = 0; state.compare_exchange_weak(expected, 1, std::memory_order_acquire))
                    return;
                spinner.yield();
            }

            // Phase 2: block using atomic wait
            for (;;) {
                if (auto expected = 0; state.compare_exchange_strong(expected, 1, std::memory_order_acquire))
                    return;
                state.wait(1, std::memory_order_relaxed); // wait while locked
            }
        }

        [[nodiscard]] bool try_lock() noexcept
        {
            int expected = 0;
            return state.compare_exchange_strong(expected, 1,
                std::memory_order_acquire);
        }

        void unlock() noexcept
        {
            state.store(0, std::memory_order_release);
            state.notify_one();
        }

        // -------------------------------------------------------------------------
        // Timed lock
        // -------------------------------------------------------------------------
        template<class Rep, class Period>
        [[nodiscard]] bool try_lock_for(const std::chrono::duration<Rep, Period>& timeout_duration) noexcept
        {
            return try_lock_until(std::chrono::steady_clock::now() + timeout_duration);
        }

        template<class Clock, class Duration>
        [[nodiscard]] bool try_lock_until(const std::chrono::time_point<Clock, Duration>& timeout_time) noexcept
        {
            // Phase 1: short spin with periodic timeout check
            spin_wait spinner;
            int iteration = 0;
            while (Clock::now() < timeout_time) {
                int expected = 0;
                if (state.compare_exchange_weak(expected, 1,
                    std::memory_order_acquire))
                    return true;

                spinner.yield();
                if ((++iteration & 0xF) == 0 && Clock::now() >= timeout_time)
                    return false;
            }
            if (Clock::now() >= timeout_time)
                return false;

            // Phase 2: timed wait
            while (Clock::now() < timeout_time) {
                int expected = 0;
                if (state.compare_exchange_strong(expected, 1,
                    std::memory_order_acquire))
                    return true;

                auto now = Clock::now();
                if (now >= timeout_time)
                    return false;

                state.wait(1, std::memory_order_relaxed);
            }
            return false;
        }

    private:
        std::atomic<int> state; // 0 = unlocked, 1 = locked
    };
}
