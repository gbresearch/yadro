    struct winpipe_server_t : winpipe_base_t
    {
        winpipe_server_t(const std::wstring& pipename, auto&& ...log_args);

        winpipe_server_t(winpipe_server_t&& other) : winpipe_base_t(static_cast<winpipe_base_t&&>(other)) {}
        
        ~winpipe_server_t() noexcept
        {
            try { log("server destructor"); }
            catch (...) {}

            if (_pipe.valid())
            {
                DisconnectNamedPipe(_pipe);
                _pipe.reset();
            }
        }

        // functions are invoked by index
        template<class ...Fn>
            requires(!(named_server_function_tuple_c<Fn> || ...))
        int run(Fn&&... functions)
        {
            if (_pipe == INVALID_HANDLE_VALUE)
                throw util::exception_t("can't run unconnected server");

            static_assert(sizeof...(Fn) != 0, "server must have at least one function");
            auto fun_tuple = std::forward_as_tuple(functions...);

            for (;;)
            {
                auto fun_index = receive<std::uint32_t>();

                if (fun_index == server_shutdown)
                {
                    log("client requested shutdown");
                    return server_shutdown;
                }

                if (fun_index == server_disconnect)
                {
                    log("client requested disconnect");
                    return 0;
                }

                if (fun_index >= sizeof...(functions))
                {
                    log("error: client requested invalid function: ", fun_index);
                    return 0;
                }

                log("client requested function: ", fun_index);

                std::size_t current_index = 0;
                util::tuple_visit(fun_tuple,
                    [&](auto&&) { return current_index++ == fun_index; },
                    [&](auto&& fn)
                    {
                        using lambda_type = std::remove_cvref_t<decltype(fn)>; // tuple
                        auto params = receive<callable_pure_args_t<lambda_type>>();
                        log("received parameters for function: ", fun_index);
                        using sent_t = std::expected< callable_return_t<lambda_type>, std::string>;

                        try
                        {
                            if constexpr (std::is_void_v<callable_return_t<lambda_type>>)
                            {
                                std::apply([&](auto&& ...args)
                                    {
                                        fn(std::move(args)...); // no response required
                                    }, params);
                                send(sent_t{});
                            }
                            else
                                send(sent_t{ std::apply([&](auto&& ...args)
                                {
                                    return fn(std::move(args)...);
                                }, params) });
                            log("sent response from function: ", fun_index);
                        }
                        catch (util::exception_t<>& e)
                        {
                            log("exception: ", e.what());
                            send(sent_t{ std::unexpected{e.what()} });
                        }
                        catch (std::exception& e)
                        {
                            log("exception: ", e.what());
                            send(sent_t{ std::unexpected{e.what()} });
                        }
                        catch (...)
                        {
                            log("unknown exception");
                            send(sent_t{ std::unexpected{std::string("unknown exception")} });
                        }

                    });
            }
        }

        // functions are invoked by name
        template<named_server_function_tuple_c ...Fn>
        int run(Fn&&... functions)
        {
            if (_pipe == INVALID_HANDLE_VALUE)
                throw util::exception_t("can't run unconnected server");

            static_assert(sizeof...(Fn) != 0, "server must have at least one function");
            auto tuple_functions = std::forward_as_tuple(functions...);
            for (;;)
            {
                auto call_id = receive<std::uint32_t>();

                if (call_id == server_shutdown)
                {
                    log("client requested shutdown");
                    return server_shutdown;
                }

                if (call_id == server_disconnect)
                {
                    log("client requested disconnect");
                    return 0;
                }

                auto fun_name = receive<std::string>();
                log("client requested function: ", fun_name);

                if (!tuple_any(tuple_functions, [&](auto&& fun_tup) { return std::get<0>(fun_tup) == fun_name; }))
                {
                    auto message = std::format("unknown pipe function: {}", fun_name);
                    log("error: ", message);
                    discard_receive_frame();
                    send(std::expected<void, std::string>{ std::unexpected{std::move(message)} });
                    continue;
                }

                try
                {
                    util::tuple_visit(tuple_functions,
                        [&](auto&& fun_tup) { return std::get<0>(fun_tup) == fun_name; },
                        [&](auto&& fn_tuple)
                        {
                            auto&& fn = std::get<1>(fn_tuple);
                            using lambda_type = std::remove_cvref_t<decltype(fn)>; // tuple
                            using sent_t = std::expected< callable_return_t<lambda_type>, std::string>;

                            try
                            {
                                auto params = receive<callable_pure_args_t<lambda_type>>();
                                log("received parameters for function: ", fun_name);

                                if constexpr (std::is_void_v<callable_return_t<lambda_type>>)
                                {
                                    std::apply([&](auto&& ...args)
                                        {
                                            fn(std::move(args)...); // no response required
                                        }, params);
                                    send(sent_t{});
                                }
                                else
                                    send(sent_t{ std::apply([&](auto&& ...args)
                                    {
                                        return fn(std::move(args)...);
                                    }, params) });
                                log("sent response from function: ", fun_name);
                            }
                            catch (util::exception_t<>& e)
                            {
                                log("exception: ", e.what());
                                send(sent_t{ std::unexpected{e.what()} });
                            }
                            catch (std::exception& e)
                            {
                                log("exception: ", e.what());
                                send(sent_t{ std::unexpected{e.what()} });
                            }
                            catch (...)
                            {
                                log("unknown exception");
                                send(sent_t{ std::unexpected{std::string("unknown exception")} });
                            }
                        });
                }
                catch (util::exception_t<>& e)
                {
                    log("exception: ", e.what());
                    send(std::expected<void, std::string>{ std::unexpected{e.what()} });
                }
                catch (std::exception& e)
                {
                    log("exception: ", e.what());
                    send(std::expected<void, std::string>{ std::unexpected{e.what()} });
                }
                catch (...)
                {
                    log("unknown exception");
                    send(std::expected<void, std::string>{ std::unexpected{ "unknown exception" }});
                }
            }
        }

        static std::optional<winpipe_server_t> accept(const std::wstring& pipename, HANDLE shutdown_event, std::shared_ptr<util::logger> log = nullptr);

    private:
        winpipe_server_t(unique_win_handle pipe, std::shared_ptr<util::logger> log)
            : winpipe_base_t(std::move(log))
        {
            _pipe = std::move(pipe);
        }
    };

    inline auto run_pipe_functions(winpipe_server_t& server, auto& functions)
    {
        return std::apply([&](auto&... fn) { return server.run(fn...); }, functions);
    }

    inline std::optional<unique_win_handle> connect_pipe_instance(const std::wstring& pipename, HANDLE shutdown_event, std::shared_ptr<util::logger> log)
    {
        auto log_pipe = [&](HANDLE pipe, auto&&... args)
            {
                if (log)
                    log->writeln(util::time_stamp(), ':', pipe, ':', std::forward<decltype(args)>(args)...);
            };

        const DWORD buf_size = static_cast<DWORD>(pipe_chunk_size); // Windows doesn't have to honor it
        unique_win_handle pipe{ CreateNamedPipe(
            pipename.c_str(),             // pipe name
            PIPE_ACCESS_DUPLEX | FILE_FLAG_OVERLAPPED,
            pipe_mode,                    // byte type, byte-read mode, blocking mode
            PIPE_UNLIMITED_INSTANCES,     // max. instances (255)
            buf_size,                     // output buffer size (default buffer size for Windows named pipes is 64 KB, above not guaranteed)
            buf_size,                     // input buffer size
            NMPWAIT_WAIT_FOREVER,         // client time-out in ms
            nullptr) };                   // default security attribute

        if (!pipe.valid())
        {
            auto str_name = pipe_name_for_error(pipename);
            log_pipe(pipe.get(), "failed to create pipe: ", str_name, ": ", GetLastError());
            throw util::exception_t("failed to create pipe: " + str_name, GetLastError());
        }

        log_pipe(pipe.get(), "server created a pipe");

        auto event = create_pipe_event();
        OVERLAPPED overlapped{};
        overlapped.hEvent = event.get();

        const auto connected = ConnectNamedPipe(pipe.get(), &overlapped);
        if (connected)
        {
            log_pipe(pipe.get(), "server connected client");
            return std::move(pipe);
        }

        auto last_error = GetLastError();
        if (last_error == ERROR_PIPE_CONNECTED)
        {
            log_pipe(pipe.get(), "server connected client");
            return std::move(pipe);
        }

        if (last_error != ERROR_IO_PENDING)
        {
            log_pipe(pipe.get(), "failed to connect to pipe: ", last_error);
            throw util::exception_t("failed to connect to pipe: ", last_error);
        }

        HANDLE events[] = { event.get(), shutdown_event };
        const auto event_count = shutdown_event != nullptr ? 2u : 1u;
        const auto wait_result = WaitForMultipleObjects(event_count, events, FALSE, INFINITE);

        if (wait_result == WAIT_OBJECT_0)
        {
            DWORD ignored{};
            if (!GetOverlappedResult(pipe.get(), &overlapped, &ignored, FALSE))
            {
                last_error = GetLastError();
                log_pipe(pipe.get(), "failed to complete pipe connection: ", last_error);
                throw util::exception_t("failed to complete pipe connection: ", last_error);
            }

            log_pipe(pipe.get(), "server connected client");
            return std::move(pipe);
        }

        if (event_count == 2u && wait_result == WAIT_OBJECT_0 + 1)
        {
            CancelIoEx(pipe.get(), &overlapped);
            WaitForSingleObject(event.get(), INFINITE);
            return std::nullopt;
        }

        last_error = GetLastError();
        log_pipe(pipe.get(), "failed waiting for pipe connection: ", last_error);
        throw util::exception_t("failed waiting for pipe connection: ", last_error);
    }

    //----------------------------------------------------------------------------------------------
    inline winpipe_server_t::winpipe_server_t(const std::wstring& pipename, auto&& ...log_args)
        : winpipe_base_t(std::forward<decltype(log_args)>(log_args)...)
    {
        auto connected_pipe = connect_pipe_instance(pipename, nullptr, _log);
        if (!connected_pipe)
            throw util::exception_t("pipe connection was cancelled without a shutdown event");

        _pipe = std::move(*connected_pipe);
    }

    inline std::optional<winpipe_server_t> winpipe_server_t::accept(const std::wstring& pipename, HANDLE shutdown_event, std::shared_ptr<util::logger> log)
    {
        auto connected_pipe = connect_pipe_instance(pipename, shutdown_event, log);
        if (!connected_pipe)
            return std::nullopt;

        return winpipe_server_t{ std::move(*connected_pipe), std::move(log) };
    }

    //----------------------------------------------------------------------------------------------
    inline auto is_server_running(const std::wstring& pipename)
    {
        auto mutex_name = pipe_server_mutex_name(pipename);
        global_mutex mtx{ mutex_name };
        return !std::unique_lock(mtx, std::defer_lock).try_lock();
    }

    //----------------------------------------------------------------------------------------------
    template< class Rep, class Period >
    inline auto is_server_running(const std::wstring& pipename, const std::chrono::duration<Rep, Period>& timeout_duration)
    {
        using namespace std::chrono_literals;
        const auto sleep_duration = 100ms;
        
        for(auto start = std::chrono::high_resolution_clock::now(); !is_server_running(pipename)
            && std::chrono::high_resolution_clock::now() - start < timeout_duration;)
            std::this_thread::sleep_for(sleep_duration);

        return is_server_running(pipename);
    }

    //----------------------------------------------------------------------------------------------
    // Function objects are shared across connection handlers and can be invoked concurrently.
    // Synchronize mutable captures inside handlers, or pass stateless callables.
    template<class ...Fn>
    void start_server(const std::wstring& pipename, std::shared_ptr<util::logger> log, Fn&&... fn)
    {
        auto max_threads = std::thread::hardware_concurrency();
        if (max_threads < 2)
            max_threads = 2;

        gb::yadro::async::threadpool tp(max_threads);
        start_server(tp, pipename, std::move(log), std::forward<Fn>(fn)...);
    }

    //----------------------------------------------------------------------------------------------
    // pipe puts new connections in threadpool
    template<class TreadPool, class ...Fn>
    void start_server(TreadPool& tp, const std::wstring& pipename, std::shared_ptr<util::logger> log, Fn&&... fn)
    {
        auto mutex_name = pipe_server_mutex_name(pipename);
        global_mutex mtx{ mutex_name };
        std::unique_lock l(mtx, std::defer_lock);
        using namespace std::chrono_literals;

        if (l.try_lock_for(100ms))
        {
            auto state = std::make_shared<winpipe_server_state>();
            auto functions = std::make_shared<std::tuple<std::decay_t<Fn>...>>(std::forward<Fn>(fn)...);

            while (!state->shutdown)
            {
                {
                    std::unique_lock lock{ state->active_connections_mutex };
                    state->active_connections_changed.wait(lock, [&] {
                        return state->shutdown || state->active_connections < max_pending_pipe_connections;
                        });

                    if (state->shutdown)
                        break;
                }

                auto server = winpipe_server_t::accept(pipename, state->shutdown_event.get(), log);
                if (!server)
                    break;

                {
                    std::lock_guard lock{ state->active_connections_mutex };
                    if (state->shutdown)
                        break;
                    ++state->active_connections;
                }

                try
                {
                    static_cast<void>(tp([state, functions, s = std::move(*server)]() mutable {
                        try
                        {
                            auto ret = run_pipe_functions(s, *functions);
                            if (ret == server_shutdown)
                                state->request_shutdown();
                        }
                        catch (...)
                        {}

                        {
                            std::lock_guard lock{ state->active_connections_mutex };
                            --state->active_connections;
                        }
                        state->active_connections_changed.notify_all();
                        }));
                }
                catch (...)
                {
                    {
                        std::lock_guard lock{ state->active_connections_mutex };
                        --state->active_connections;
                    }
                    state->active_connections_changed.notify_all();
                    throw;
                }
            }

            std::unique_lock lock{ state->active_connections_mutex };
            state->active_connections_changed.wait(lock, [&] { return state->active_connections == 0; });
        }
        else
        {
            auto message = to_string("failed to start the server another instance is already running, mutex: ", util::from_wstring(mutex_name));
            throw_error(message);
        }
    }
    //----------------------------------------------------------------------------------------------
    // pipe puts new connections in threadpool
    template<class ...Fn>
    void start_server(std::size_t max_threads, const std::wstring& pipename, std::shared_ptr<util::logger> log, Fn&&... fn)
    {
        using namespace std::chrono_literals;
        gb::yadro::async::threadpool tp(max_threads);
        start_server(tp, pipename, log, std::forward<decltype(fn)>(fn)...);
    }

    //----------------------------------------------------------------------------------------------
    inline bool shutdown_server(const std::wstring& pipename, unsigned attempts, auto&&...log_args)
    {
        using namespace std::chrono_literals;
        constexpr auto shutdown_timeout = 5s;
        std::string last_error;
        const auto deadline = std::chrono::steady_clock::now() + shutdown_timeout;

        while (std::chrono::steady_clock::now() < deadline && is_server_running(pipename, 100ms))
        {
            try
            {
                winpipe_client_t(pipename, "shutdown", attempts, log_args...).shutdown();
            }
            catch (std::exception& e)
            {
                last_error = e.what();
            }
            catch (...)
            {
                last_error = "unknown shutdown exception";
            }

            std::this_thread::sleep_for(50ms);
        }

        const auto stopped = !is_server_running(pipename);
        if (!stopped && !last_error.empty())
        {
            try { logger{ log_args... }.writeln("failed to shutdown pipe server: ", pipe_name_for_error(pipename), ": ", last_error); }
            catch (...) {}
        }

        return stopped;
    }
