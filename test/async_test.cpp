    GB_TEST(async, async_test_legacy)
    {
        legacy::threadpool tp;
        auto f1 = tp([] 
            { 
                auto sum{ 0. }; 
                for (auto i = 0u; i < 100'000'000; ++i) ++sum; 
                return sum; 
            }).share();
        
        auto f2 = tp([](auto init) 
            { 
                for (auto i = 0u; i < 100'000'000; ++i) ++init; 
                return init;
            }, 0.);

        auto f3 = tp.then([](auto init)
            {
                for (auto i = 0u; i < 100'000'000; ++i) --init;
                return init;
            }, f1).share();

        auto fvoid = tp.then([](auto v) { gbassert(v == 0); }, f3);
        static_assert(std::is_same_v<decltype(fvoid), std::future<void>>);

        auto f = tp.then([](auto v1, auto v2, auto v3) { return v1 == v2 && v3 == 0; }, f1, std::move(f2), f3).share();
        auto ff = tp.then([](auto v) { return v ? 1 : 0; }, f);
        
        fvoid.get();
        gbassert(f.get());
        gbassert(ff.get() == 1);
    }

