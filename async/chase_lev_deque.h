/**
 * @file chase_lev_deque.h
 * @brief Lock-free Chase-Lev Work-Stealing Deque (C++23)
 *
 * Based on:
 *   [CL05] David Chase & Yossi Lev, "Dynamic Circular Work-Stealing Deque" (SPAA 2005)
 *   [LeN13] Nhat Minh Lê et al., "Correct and Efficient Work-Stealing for Weak
 *           Memory Models" (PPoPP 2013) — memory-order correctness fixes
 *
 * Ownership model
 * ───────────────
 *   • Only the *owner* thread may call push_bottom() / pop_bottom().
 *   • Any thread (including the owner) may call steal().
 *
 * Guarantees
 * ──────────
 *   • Wait-free push_bottom / pop_bottom for the owner.
 *   • Lock-free steal for thieves.
 *   • Linearisable: every operation appears to take effect at a single point.
 *   • ABA-free on the index pair (top, bottom): indices only ever advance.
 *
 * Memory reclamation
 * ──────────────────
 *   Old CircularArray buffers (produced by resize) are held in gc_list_ and
 *   freed when the deque is destroyed.  This is safe because:
 *     a) The owner is the only thread that resizes (single writer on array_).
 *     b) A thief that loaded the old array pointer before the resize still
 *        holds a valid pointer: the old array is never freed while the deque
 *        is alive, and the deque outlives all threads that can call steal().
 *   The cost is O(resize count) memory retention for the deque's lifetime.
 *   For long-lived pools that resize many times, replace gc_list_ with a
 *   hazard-pointer or epoch-based reclamation scheme, or call
 *   garbage_collect() from the owner thread during a quiescent period when
 *   no thief can be mid-steal (e.g., when the pool is completely idle).
 *
 * Type requirements
 * ─────────────────
 *   T must be trivially copyable (enforced by static_assert).  For complex
 *   task objects, store a pointer (e.g. Task*) — exactly as threadpool does —
 *   so the deque stores only the cheap, trivially-copyable pointer.
 *
 * Why seq_cst fences and not just acquire/release?
 * ─────────────────────────────────────────────────
 *   The matching seq_cst fence in pop_bottom() and steal() is the key
 *   correctness contribution of [LeN13].  On weak-memory architectures
 *   (ARM, POWER, RISC-V) an acquire/release pair alone is insufficient:
 *   both the owner and a thief could simultaneously observe "the last slot
 *   is mine" because the relaxed top_ load in pop_bottom() is not ordered
 *   against steal()'s top_ load without a fence that participates in the
 *   seq_cst total order.  Weakening these fences reintroduces the original
 *   Chase-Lev double-claim bug on non-TSO platforms.  Do not change them.
 */

#pragma once

#include <atomic>
#include <cstdint>
#include <memory>
#include <new>       // std::hardware_destructive_interference_size
#include <utility>
#include <vector>

namespace gb::yadro::async
{
    // ─────────────────────────────────────────────────────────────────────────────
    // Internal: growable circular array
    // ─────────────────────────────────────────────────────────────────────────────

    /**
     * @brief Power-of-two ring buffer used internally by WorkStealingDeque.
     *
     * Indices wrap naturally via bitmask.  The array itself is NOT thread-safe —
     * callers are responsible for the correct memory ordering on the slots they
     * read/write.
     */
    template <typename T>
    class CircularArray {
    public:
        explicit CircularArray(std::size_t log2_capacity)
            : log2_cap_{ log2_capacity }
            , mask_{ (std::size_t{1} << log2_capacity) - 1 }
            , storage_{ std::make_unique<std::atomic<T>[]>(std::size_t{1} << log2_capacity) }
        {
        }

        // Non-copyable, non-movable (owned by raw pointer inside the deque)
        CircularArray(const CircularArray&) = delete;
        CircularArray& operator=(const CircularArray&) = delete;

        [[nodiscard]] std::size_t capacity() const noexcept {
            return std::size_t{ 1 } << log2_cap_;
        }

        /// Store a value at logical index i (owner side — relaxed is fine here;
        /// the surrounding deque operations supply the required fences).
        void store(std::int64_t i, T val) noexcept {
            storage_[static_cast<std::size_t>(i) & mask_]
                .store(std::move(val), std::memory_order_relaxed);
        }

        /// Load the value at logical index i.
        [[nodiscard]] T load(std::int64_t i) const noexcept {
            return storage_[static_cast<std::size_t>(i) & mask_]
                .load(std::memory_order_relaxed);
        }

        /**
         * @brief Grow into a new CircularArray of twice the capacity.
         *
         * Copies all live elements [top, bottom) from the current buffer into the
         * new one.  Returns the new buffer; the caller is responsible for pushing
         * the old buffer onto gc_list_ before publishing the new pointer.
         */
        [[nodiscard]] CircularArray* grow(std::int64_t bottom,
            std::int64_t top) const {
            auto* next = new CircularArray{ log2_cap_ + 1 };
            for (std::int64_t i = top; i < bottom; ++i)
                next->store(i, load(i));
            return next;
        }

    private:
        std::size_t                        log2_cap_;
        std::size_t                        mask_;
        std::unique_ptr<std::atomic<T>[]>  storage_;
    };

    // ─────────────────────────────────────────────────────────────────────────────
    // Result type returned by steal() / pop_bottom()
    // ─────────────────────────────────────────────────────────────────────────────

    /// Discriminated result: a stolen/popped item, or one of two empty states.
    enum class StealResult { Success, Empty, Abort };

    // ─────────────────────────────────────────────────────────────────────────────
    // WorkStealingDeque
    // ─────────────────────────────────────────────────────────────────────────────

    /**
     * @brief Lock-free Chase-Lev work-stealing deque.
     *
     * @tparam T              Element type.  Must be trivially copyable (enforced by
     *                        static_assert).  For complex task objects store a pointer.
     * @tparam InitialLog2Cap log₂ of the initial ring-buffer capacity (default 8 → 256 slots).
     */
    template <typename T, std::size_t InitialLog2Cap = 8>
    class WorkStealingDeque {
        // ── Type requirements ──────────────────────────────────────────────────
        //
        // std::atomic<T> requires T to be trivially copyable per the C++ standard
        // [atomics.types.generic].  Violating this is undefined behaviour.
        // Enforce it here so a helpful diagnostic fires at the point of instantiation
        // rather than deep inside a system header.
        static_assert(std::is_trivially_copyable_v<T>,
            "WorkStealingDeque<T>: T must be trivially copyable. "
            "Wrap complex task types in a pointer (e.g. Task*).");

        static_assert(InitialLog2Cap >= 1 && InitialLog2Cap <= 30,
            "Capacity exponent must be in [1, 30]");

    public:
        // ── Types ──────────────────────────────────────────────────────────────

        using value_type = T;

        struct PopResult {
            StealResult status;
            T           value{};  // valid only when status == Success
        };

        // ── Construction / destruction ─────────────────────────────────────────

        WorkStealingDeque()
            : top_{ 0 }
            , bottom_{ 0 }
            , array_{ new CircularArray<T>{InitialLog2Cap} }
        {
        }

        ~WorkStealingDeque() {
            // Safe: the destructor is only called once all threads that could
            // call steal() have been joined (e.g. the threadpool destructor joins
            // workers before destroying deques).  No concurrent access is possible
            // at this point, so relaxed loads and plain deletes are correct.
            delete array_.load(std::memory_order_relaxed);
            for (auto* p : gc_list_)
                delete p;
        }

        WorkStealingDeque(const WorkStealingDeque&) = delete;
        WorkStealingDeque& operator=(const WorkStealingDeque&) = delete;
        WorkStealingDeque(WorkStealingDeque&&) = delete;
        WorkStealingDeque& operator=(WorkStealingDeque&&) = delete;

        // ── Owner-only operations ──────────────────────────────────────────────

        /**
         * @brief Push a task onto the bottom (private) end.
         *
         * Called exclusively by the owning thread.  Wait-free, may trigger a
         * single O(n) buffer resize when the deque is full.
         *
         * Memory order synopsis (matching [LeN13] §4):
         *   1. Relaxed load of bottom  — we wrote it last, no other writer.
         *   2. Acquire load of top     — to check fullness against concurrent steals.
         *   3. Release store of array_ — makes the new buffer visible before slots.
         *   4. Relaxed slot store      — ordering supplied by the release fence below.
         *   5. Release fence           — ensures slot write is visible before bottom.
         *   6. Relaxed store of bottom — no other thread writes bottom.
         */
        void push_bottom(T task) noexcept {
            const std::int64_t b = bottom_.load(std::memory_order_relaxed);
            const std::int64_t t = top_.load(std::memory_order_acquire);
            auto* arr = array_.load(std::memory_order_relaxed);

            // Resize if needed (owner is the only writer, so this is safe).
            if (b - t > static_cast<std::int64_t>(arr->capacity()) - 1) {
                auto* grown = arr->grow(b, t);
                gc_list_.push_back(arr);              // retain: a thief may still hold arr
                array_.store(grown, std::memory_order_release);
                arr = grown;
            }

            arr->store(b, std::move(task));

            // Publish b+1 to potential thieves: the slot write must be visible
            // before the updated bottom is.
            std::atomic_thread_fence(std::memory_order_release);
            bottom_.store(b + 1, std::memory_order_relaxed);
        }

        /**
         * @brief Pop a task from the bottom (private) end.
         *
         * Called exclusively by the owning thread.  Wait-free.
         *
         * Memory order synopsis:
         *   1. Relaxed decrement of bottom  — we own bottom_.
         *   2. seq_cst fence               — forms a total order with steal()'s
         *                                    seq_cst fence (see file-level note).
         *   3. Relaxed load of top         — safe after the seq_cst fence.
         *   4. If deque non-empty: return; restore bottom_ with a CAS if needed.
         *   5. Reset bottom_ to t+1 on conflict (deque emptied by a thief).
         */
        [[nodiscard]] PopResult pop_bottom() noexcept {
            std::int64_t b = bottom_.load(std::memory_order_relaxed) - 1;
            auto* arr = array_.load(std::memory_order_relaxed);

            bottom_.store(b, std::memory_order_relaxed);

            // seq_cst fence: pairs with the seq_cst fence in steal().
            // Together they prevent both the owner and a thief from simultaneously
            // believing they have claimed the last element (see [LeN13] §4 and the
            // file-level rationale — do NOT weaken this to acquire/release).
            std::atomic_thread_fence(std::memory_order_seq_cst);

            std::int64_t t = top_.load(std::memory_order_relaxed);

            if (t > b) {
                // Deque was already empty before we decremented; restore.
                bottom_.store(b + 1, std::memory_order_relaxed);
                return { StealResult::Empty };
            }

            T val = arr->load(b);

            if (t < b) {
                // No contention: more than one element remains; no CAS needed.
                return { StealResult::Success, std::move(val) };
            }

            // t == b: exactly one element — race with concurrent steal().
            // Use CAS on top to atomically claim it.
            const bool won = top_.compare_exchange_strong(
                t, t + 1,
                std::memory_order_seq_cst,
                std::memory_order_relaxed);

            // Restore bottom_ regardless — the deque is now empty either way.
            bottom_.store(b + 1, std::memory_order_relaxed);

            if (won)
                return { StealResult::Success, std::move(val) };
            else
                return { StealResult::Empty };
        }

        // ── Thief operations (any thread) ──────────────────────────────────────

        /**
         * @brief Steal a task from the top (public) end.
         *
         * May be called from any thread concurrently.  Lock-free.
         *
         * Returns:
         *   Success — task was successfully stolen.
         *   Empty   — deque was empty at the time of the call.
         *   Abort   — a competing steal (or concurrent pop_bottom) caused a CAS
         *             failure; caller should retry or move on.
         *
         * Memory order synopsis (matching [LeN13] §4):
         *   1. Acquire load of top.
         *   2. seq_cst fence — pairs with pop_bottom()'s seq_cst fence to form a
         *                      total order preventing double-claim of last element.
         *   3. Acquire load of bottom — synchronizes-with the release fence in
         *                               push_bottom(), observing all slot writes.
         *   4. Acquire load of array_ — see "Why acquire?" note below.
         *   5. Relaxed slot read      — array_ acquire supplies the ordering.
         *   6. seq_cst CAS on top     — atomically claim the element.
         *
         * Why acquire (not relaxed or consume) for array_?
         * ─────────────────────────────────────────────────
         *   memory_order_consume was the previous choice.  It is deprecated since
         *   C++17 (P0371) because no compiler implements it correctly — GCC, Clang,
         *   and MSVC all silently promote it to acquire.  Using it gives a false
         *   sense of dependency ordering and should be avoided.
         *
         *   A relaxed load would also be technically correct here: the acquire on
         *   bottom_ (step 3) synchronizes-with the release fence + bottom_.store in
         *   push_bottom(), which in turn is sequenced after array_.store(release).
         *   That transitive chain makes the new array_ value visible.  However,
         *   a relaxed load depends on that non-obvious three-step chain holding, and
         *   a future refactor could break it silently.  acquire is explicit, safe,
         *   and self-documenting with negligible additional cost.
         */
        [[nodiscard]] PopResult steal() noexcept {
            std::int64_t t = top_.load(std::memory_order_acquire);

            // seq_cst fence: pairs with pop_bottom()'s seq_cst fence.
            // Do NOT weaken — see file-level rationale.
            std::atomic_thread_fence(std::memory_order_seq_cst);

            std::int64_t b = bottom_.load(std::memory_order_acquire);

            if (t >= b)
                return { StealResult::Empty };

            // acquire replaces the deprecated memory_order_consume.
            // See "Why acquire?" in the doc comment above.
            auto* arr = array_.load(std::memory_order_acquire);
            T     val = arr->load(t);

            if (!top_.compare_exchange_strong(
                t, t + 1,
                std::memory_order_seq_cst,
                std::memory_order_relaxed))
                return { StealResult::Abort };

            return { StealResult::Success, std::move(val) };
        }

        // ── Observers ─────────────────────────────────────────────────────────

        /**
         * @brief Approximate size.
         *
         * Not linearisable in the presence of concurrent operations; use only
         * for load-balancing heuristics.
         */
        [[nodiscard]] std::size_t size_approx() const noexcept {
            const std::int64_t b = bottom_.load(std::memory_order_relaxed);
            const std::int64_t t = top_.load(std::memory_order_relaxed);
            return static_cast<std::size_t>(b > t ? b - t : 0);
        }

        [[nodiscard]] bool empty_approx() const noexcept {
            return size_approx() == 0;
        }

        [[nodiscard]] std::size_t capacity() const noexcept {
            return array_.load(std::memory_order_relaxed)->capacity();
        }

        // ── Garbage collection ─────────────────────────────────────────────────

        /**
         * @brief Free all retired CircularArray buffers.
         *
         * SAFETY: May only be called by the owner thread during a quiescent
         * period in which it is certain that no thief is currently executing
         * steal() and holding a pointer into an old buffer.  In a thread pool
         * this is only safe when the pool is completely idle (all workers are
         * spinning in their yield loops with no tasks in flight).
         *
         * For automatic reclamation without quiescence checkpoints, replace
         * gc_list_ with a hazard-pointer or epoch-based scheme.
         */
        void garbage_collect() noexcept {
            for (auto* p : gc_list_)
                delete p;
            gc_list_.clear();
        }

        /// Number of retired arrays currently awaiting reclamation.
        [[nodiscard]] std::size_t gc_pending() const noexcept {
            return gc_list_.size();
        }

    private:
        // Use the standard cache-line size constant (C++17) rather than a
        // hard-coded 64 for correctness on platforms with wider cache lines
        // (e.g. some POWER CPUs use 128-byte lines).
#ifdef __cpp_lib_hardware_interference_size
        static constexpr std::size_t kCacheLineSize =
            std::hardware_destructive_interference_size;
#else
        static constexpr std::size_t kCacheLineSize = 64;
#endif

        // Separate hot atomic variables onto distinct cache lines to eliminate
        // false sharing between the owner (bottom_) and thieves (top_).
        alignas(kCacheLineSize) std::atomic<std::int64_t>      top_;
        alignas(kCacheLineSize) std::atomic<std::int64_t>      bottom_;
        alignas(kCacheLineSize) std::atomic<CircularArray<T>*> array_;

        // Old buffers awaiting safe reclamation.
        // Invariant: every pointer here is valid (not freed) for as long as the
        // deque is alive.  See the "Memory reclamation" section in the file header.
        std::vector<CircularArray<T>*> gc_list_;
    };

} // end namespace
