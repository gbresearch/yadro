/*
 * Augmented Dickey-Fuller (ADF) Unit Root Test
 *
 * Features:
 *   - Automatic lag selection via AIC (Schwert 1989 rule)
 *   - MacKinnon (1994, 2010) p-value approximation with finite-sample correction
 *   - Cholesky decomposition for numerical stability
 *   - Three regression specifications: none, constant, constant+trend
 *   - C++23 standard library only (no external dependencies)
 *
 * Reference:
 *   Said, S.E. and Dickey, D.A. (1984) "Testing for Unit Roots in
 *   Autoregressive-Moving Average Models of Unknown Order" Biometrika 71, 599-607.
 *
 *   MacKinnon, J.G. (1994) "Approximate asymptotic distribution functions for
 *   unit-root and cointegration tests" Journal of Business & Economic Statistics 12, 167-76.
 *
 *   MacKinnon, J.G. (2010) "Critical Values for Cointegration Tests" Queen's
 *   Economics Department Working Paper No. 1227.
 *
 * Usage:
 *   vector<double> data = {1.0, 1.5, 2.0, ..., 10.0};
 *   auto result = adfuller(data);
 *
 *   if (result.pvalue < 0.05) {
 *       cout << "Reject null hypothesis: series is stationary\n";
 *   } else {
 *       cout << "Fail to reject: series may have unit root\n";
 *   }
 *
 */

#pragma once

#include <vector>
#include "mackinnon.h"

namespace gb::yadro::algorithm 
{
    // ============================================================================
    // ADF Result Structure
    // ============================================================================

    struct ADFResult {
        double statistic;      // ADF test statistic (t-stat on lagged level coef)
        double pvalue;         // MacKinnon approximate p-value
        int best_lag;          // Optimal lag selected by AIC
        TrendType trend;       // Regression specification used
        std::size_t nobs;      // Effective observations in regression
        double gamma;          // Lagged level coefficient (< 0 for stationarity)
        double se_gamma;       // Standard error of gamma
    };

    // ============================================================================
    // Main ADF Test Function
    // ============================================================================

    /*
     * Augmented Dickey-Fuller unit root test with automatic lag selection.
     *
     * Null hypothesis: Series has a unit root (non-stationary, I(1))
     * Alternative: Series is stationary (I(0))
     *
     * Parameters:
     *   series   - Time series data (vector of doubles)
     *   max_lag  - Maximum lag for augmentation (-1 = automatic using Schwert rule)
     *   trend    - Deterministic terms in regression:
     *              NONE: no constant (rare)
     *              CONSTANT: constant only (default for most series)
     *              CONSTANT_TREND: constant + linear time trend
     *
     * Returns:
     *   ADFResult containing:
     *     - statistic: ADF t-statistic (more negative = more stationary)
     *     - pvalue: probability of observing test stat under null
     *     - best_lag: lag chosen by AIC
     *     - trend: regression specification used
     *     - nobs: effective sample size after differencing and lags
     *     - gamma: coefficient on lagged level (should be < 0 for stationarity)
     *     - se_gamma: standard error of gamma
     *
     * Decision rule:
     *   if (pvalue < 0.05) reject null ? series is stationary
     *   else fail to reject ? series may have unit root
     *
     * Typical critical values (5% level):
     *   No constant: -1.95
     *   Constant: -2.86
     *   Constant + trend: -3.41
     *
     * Throws:
     *   std::invalid_argument if series is too short, constant, or contains NaN/Inf
     */

    ADFResult adfuller(const std::vector<double>& series, int max_lag = -1, TrendType trend = TrendType::CONSTANT); 
}
