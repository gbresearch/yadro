    // ---------------- Constants ----------------
    constexpr double EPS = 1e-12; // epsilon for numerical stability
    constexpr double SCHWERT_FACTOR = 12.0; // Schwert (1989) rule-of-thumb factor

    struct CholeskyFactor {
        vector<vector<double>> L;

        explicit CholeskyFactor(const vector<vector<double>>& A);

        vector<double> solve(const vector<double>& b) const;

        vector<vector<double>> inverse() const;
    };

    // ---------------- OLS ----------------

    struct OLSResult {
        vector<double> beta;
        double s2;
        vector<vector<double>> cov;
    };

    // ---------------- Utility ----------------
    bool is_constant_series(const vector<double>& x) {
        if (x.empty()) return true;
        double first = x.front();
        return std::all_of(x.begin(), x.end(),
            [&](double v) { return std::fabs(v - first) < 1e-12; });
    }

    bool has_nan_or_inf(const vector<double>& x) {
        return std::any_of(x.begin(), x.end(),
            [](double v) { return !std::isfinite(v); });
    }

    vector<double> forward_subst(const vector<vector<double>>& L, const vector<double>& b) {
        size_t n = L.size();
        vector<double> y(n);
        for (size_t i = 0; i < n; ++i) {
            double sum = b[i];
            for (size_t j = 0; j < i; ++j) sum -= L[i][j] * y[j];
            y[i] = sum / L[i][i];
        }
        return y;
    }

    vector<double> back_subst(const vector<vector<double>>& L, const vector<double>& y) {
        size_t n = L.size();
        vector<double> x(n);
        for (int i = static_cast<int>(n) - 1; i >= 0; --i) {
            double sum = y[i];
            for (size_t j = i + 1; j < n; ++j) sum -= L[j][i] * x[j];
            x[i] = sum / L[i][i];
        }
        return x;
    }

    vector<vector<double>> cholesky_decompose(const vector<vector<double>>& A) {
        size_t n = A.size();
        vector<vector<double>> L(n, vector<double>(n, 0.0));
        for (size_t i = 0; i < n; ++i) {
            for (size_t j = 0; j <= i; ++j) {
                double sum = A[i][j];
                for (size_t k = 0; k < j; ++k) sum -= L[i][k] * L[j][k];
                if (i == j) {
                    gbassert(sum >= EPS, "Matrix not positive definite");
                    L[i][j] = std::sqrt(sum);
                }
                else {
                    L[i][j] = sum / L[j][j];
                }
            }
        }
        return L;
    }

    OLSResult ols_fit(const vector<vector<double>>& X, const vector<double>& y) {
        const size_t n = X.size();
        const size_t k = X[0].size();

        // XtX computation exploiting symmetry
        vector<vector<double>> XtX(k, vector<double>(k, 0.0));
        for (size_t i = 0; i < k; ++i)
            for (size_t j = i; j < k; ++j) {
                double sum = 0.0;
                for (size_t t = 0; t < n; ++t) sum += X[t][i] * X[t][j];
                XtX[i][j] = XtX[j][i] = sum;
            }

        // X'y
        vector<double> Xty(k, 0.0);
        for (size_t i = 0; i < k; ++i)
            for (size_t t = 0; t < n; ++t)
                Xty[i] += X[t][i] * y[t];

        // Solve via Cholesky
        CholeskyFactor chol(XtX);
        auto beta = chol.solve(Xty);
        auto XtX_inv = chol.inverse();

        // Residual variance
        vector<double> yhat(n, 0.0);
        for (size_t t = 0; t < n; ++t)
            for (size_t j = 0; j < k; ++j)
                yhat[t] += X[t][j] * beta[j];

        double ssr = 0.0;
        for (size_t t = 0; t < n; ++t) {
            double e = y[t] - yhat[t];
            ssr += e * e;
        }
        double s2 = ssr / static_cast<double>(n - k);

        return { beta, s2, XtX_inv };
    }

    // Regression builder
    vector<vector<double>> build_adf_regressors(const vector<double>& series, const vector<double>& dy, int lag, TrendType trend) {
        const size_t n = series.size();
        size_t dy_len = dy.size();
        size_t trend_cols = (trend == TrendType::CONSTANT_TREND ? 2 :
            trend == TrendType::CONSTANT ? 1 : 0);
        if (dy_len < static_cast<size_t>(lag)) return {};
        size_t nrows = dy_len - lag;
        size_t ncols = trend_cols + 1 + lag;
        vector<vector<double>> X;
        X.reserve(nrows);

        for (size_t t = lag; t < dy_len; ++t) {
            vector<double> row;
            row.reserve(ncols);
            size_t time_index = t - static_cast<size_t>(lag) + 1;

            if (trend == TrendType::CONSTANT || trend == TrendType::CONSTANT_TREND)
                row.push_back(1.0);
            if (trend == TrendType::CONSTANT_TREND)
                row.push_back(static_cast<double>(time_index));

            row.push_back(series[t]);
            for (int i = 1; i <= lag; ++i)
                row.push_back(dy[t - i]);

            X.push_back(std::move(row));
        }

        return X;
    }

    ADFResult adfuller(const vector<double>& series, int max_lag, TrendType trend)
    {
        // Input validation
        gbassert(!has_nan_or_inf(series), "adfuller: series contains NaN or Inf");
        gbassert(!is_constant_series(series), "adfuller: series is constant (zero variance)");
        const int n = static_cast<int>(series.size());
        gbassert(n >= 2, "adfuller: series too short for differencing");
        // Determine maximum lag using Schwert (1989) rule if not specified
        int default_max_lag = static_cast<int>(
            std::floor(SCHWERT_FACTOR * std::pow(static_cast<double>(n) / 100.0, 0.25))
            );
        if (default_max_lag < 0) default_max_lag = 0;
        int max_lag_use = (max_lag < 0) ? default_max_lag : max_lag;

        // Ensure sufficient observations
        gbassert(series.size() >= 5 + static_cast<size_t>(max_lag_use), "adfuller: series too short for requested max_lag");
        // Precompute first differences once (optimization)
        vector<double> dy;
        dy.reserve(series.size() - 1);
        for (size_t i = 0; i + 1 < series.size(); ++i)
            dy.push_back(series[i + 1] - series[i]);

        // AIC-based lag selection
        double best_aic = std::numeric_limits<double>::infinity();
        double best_stat = std::numeric_limits<double>::quiet_NaN();
        double best_gamma = 0.0;
        double best_se_gamma = 0.0;
        int best_lag = 0;
        size_t best_nobs = 0;

        // Search over lags from 0 to max_lag_use
        for (int lag = 0; lag <= max_lag_use; ++lag) {
            try {
                // Build design matrix for this lag
                auto X = build_adf_regressors(series, dy, lag, trend);
                if (X.empty()) continue;

                // Build dependent variable (differences from lag onward)
                vector<double> y;
                y.reserve(dy.size() - static_cast<size_t>(lag));
                for (size_t i = static_cast<size_t>(lag); i < dy.size(); ++i)
                    y.push_back(dy[i]);

                // Run OLS regression
                auto ols = ols_fit(X, y);

                // Extract coefficient on lagged level (gamma)
                size_t beta_idx = (trend == TrendType::CONSTANT_TREND ? 2 :
                    trend == TrendType::CONSTANT ? 1 : 0);

                gbassert(beta_idx < ols.beta.size(), "adfuller: internal error - beta index out of range");
                double gamma = ols.beta[beta_idx];
                double var_gamma = ols.cov[beta_idx][beta_idx];

                gbassert(var_gamma >= 0.0, "adfuller: negative variance estimate (numerical instability)");
                double se_gamma = std::sqrt(var_gamma);
                double t_stat = gamma / (se_gamma > 0.0 ? se_gamma : 1e-18);

                // Compute AIC: n * log(SSR/n) + 2k
                double n_eff = static_cast<double>(y.size());
                double k = static_cast<double>(X[0].size());
                double ssr = ols.s2 * (n_eff - k);
                double aic = n_eff * std::log(ssr / n_eff) + 2.0 * k;

                // Update best model if AIC improved
                if (aic < best_aic) {
                    best_aic = aic;
                    best_stat = t_stat;
                    best_lag = lag;
                    best_gamma = gamma;
                    best_se_gamma = se_gamma;
                    best_nobs = y.size();
                }
            }
            catch (const std::exception& e) {
                // Skip this lag if regression fails (e.g., singular matrix)
#ifndef NDEBUG
                std::cerr << "adfuller: skipped lag " << lag << ": " << e.what() << "\n";
#else
                (void)e;
#endif
                continue;
            }
        }

        // Check that at least one lag produced valid results
        gbassert(best_nobs != 0, "adfuller: no valid lag produced solvable OLS (try reducing max_lag)");
        // Compute MacKinnon approximate p-value
        double pvalue = mackinnon_pvalue_fast(best_stat, static_cast<int>(best_nobs), trend);

        return { best_stat, pvalue, best_lag, trend, best_nobs, best_gamma, best_se_gamma };
    }

    // ---------------- Linear Algebra ----------------

    inline CholeskyFactor::CholeskyFactor(const vector<vector<double>>& A)
        : L(cholesky_decompose(A)) {
    }

    inline vector<double> CholeskyFactor::solve(const vector<double>& b) const {
        auto y = forward_subst(L, b);
        return back_subst(L, y);
    }

    inline vector<vector<double>> CholeskyFactor::inverse() const {
        size_t k = L.size();
        vector<vector<double>> inv(k, vector<double>(k, 0.0));
        for (size_t j = 0; j < k; ++j) {
            vector<double> ej(k, 0.0); ej[j] = 1.0;
            auto col = solve(ej);
            for (size_t i = 0; i < k; ++i)
                inv[i][j] = col[i];
        }
        return inv;
    }

