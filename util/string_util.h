    constexpr auto base64_chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789+/";
    inline bool is_base64(unsigned char c) { return std::isalnum(c) || c == '+' || c == '/'; }
    //-------------------------------------------------------------------------
    // base64 encoding (binary to ASCII subset)
    inline auto base64_encode(std::ranges::sized_range auto&& binary_buffer)
    {
        auto byte_span = std::as_bytes(std::span(binary_buffer));
        auto length = byte_span.size_bytes();
        auto buffer = byte_span.data();

        std::string encoded;
        encoded.reserve((length + 2) / 3 * 4);

        for (size_t i = 0; i < length; i += 3) {
            std::uint32_t group = std::to_integer<std::uint32_t>(buffer[i]) << 16;

            if (i + 1 < length)
                group |= std::to_integer<std::uint32_t>(buffer[i + 1]) << 8;
            if (i + 2 < length)
                group |= std::to_integer<std::uint32_t>(buffer[i + 2]);

            encoded += base64_chars[(group >> 18) & 0x3F];
            encoded += base64_chars[(group >> 12) & 0x3F];

            if (i + 1 < length)
                encoded += base64_chars[(group >> 6) & 0x3F];
            else
                encoded += '=';

            if (i + 2 < length)
                encoded += base64_chars[group & 0x3F];
            else
                encoded += '=';
        }

        return encoded;
    }

    //-------------------------------------------------------------------------
    // base64 encoding (binary to ASCII subset)
    inline auto base64_encode(const char* str)
    {
        return base64_encode(std::string_view(str));
    }

    //-------------------------------------------------------------------------
    // base64 encoding (binary to ASCII subset)
    template<class T>
    inline auto base64_encode(const T& t) requires(std::is_aggregate_v<T> or std::is_fundamental_v<T>)
    {
        return base64_encode(std::span(std::addressof(t), 1));
    }

    //-------------------------------------------------------------------------
    // base64 decoding, returns vector<unsigned char>
    inline auto base64_decode(const std::string& encoded_string) 
    {
        std::vector<unsigned char> ret;

        int i = 0;
        unsigned char char_array_4[4], char_array_3[3];

        for (std::size_t in_len = encoded_string.size(), idx = 0;
            --in_len && (encoded_string[idx] != '=') && is_base64(encoded_string[idx]);)
        {
            char_array_4[i++] = encoded_string[idx]; 
            ++idx;
            if (i == 4) 
            {
                for (i = 0; i < 4; i++)
                    char_array_4[i] = static_cast<unsigned char>(std::string_view(base64_chars).find(char_array_4[i]));

                char_array_3[0] = (char_array_4[0] << 2) + ((char_array_4[1] & 0x30) >> 4);
                char_array_3[1] = ((char_array_4[1] & 0xf) << 4) + ((char_array_4[2] & 0x3c) >> 2);
                char_array_3[2] = ((char_array_4[2] & 0x3) << 6) + char_array_4[3];

                for (i = 0; (i < 3); i++)
                    ret.push_back(char_array_3[i]);
                i = 0;
            }
        }

        if (i) 
        {
            for (auto j = i; j < 4; j++)
                char_array_4[j] = 0;

            for (auto j = 0; j < 4; j++)
                char_array_4[j] = static_cast<unsigned char>(std::string_view(base64_chars).find(char_array_4[j]));

            char_array_3[0] = (char_array_4[0] << 2) + ((char_array_4[1] & 0x30) >> 4);
            char_array_3[1] = ((char_array_4[1] & 0xf) << 4) + ((char_array_4[2] & 0x3c) >> 2);
            char_array_3[2] = ((char_array_4[2] & 0x3) << 6) + char_array_4[3];

            for (auto j = 0; (j < i - 1); j++) ret.push_back(char_array_3[j]);
        }

        return ret;
    }

    //-------------------------------------------------------------------------
    // tokenize string based on specified delimiter
    //-------------------------------------------------------------------------
    template<class T>
    inline auto tokenize(const std::basic_string<T>& input, T separator)
    {
        std::vector<std::basic_string<T>> tokens;
        std::basic_istringstream<T> token_stream(input);

        for (std::string token; std::getline(token_stream, token, separator);)
            tokens.push_back(token);

        return tokens;
    }

    //---------------------
    // span 3-way comparison
    template<class T1, std::size_t Extent1, class T2, std::size_t Extent2>
    auto compare(const std::span<T1, Extent1>& s1, const std::span<T2, Extent2>& s2) requires(std::three_way_comparable_with<T1, T2>)
    {
        using ordering_t = std::compare_three_way_result_t<T1, T2>;
        if (s1.size() < s2.size())
            return ordering_t::less;
        else if (s1.size() > s2.size())
            return ordering_t::greater;
        else for (std::size_t i = 0; i < s1.size(); ++i)
        {
            auto cmp = s1[i] <=> s2[i];
            if (std::is_neq(cmp))
                return cmp;
        }
        return ordering_t::equivalent;
    }

    //---------------------
    template<class CharT, class Traits>
    auto compare(const CharT* s1, const CharT* s2, std::size_t size1, std::size_t size2)
    {
        auto comp = Traits::compare(s1, s2, std::min(size1, size2));
        return comp < 0 ? std::weak_ordering::less
            : comp > 0 ? std::weak_ordering::greater
            : size1 < size2 ? std::weak_ordering::less
            : size1 > size2 ? std::weak_ordering::greater
            : std::weak_ordering::equivalent;
    }

    //-------------------------------------------------------------------------
    constexpr auto strings_equal(const char* s1, const char* s2)
    {
        return std::char_traits<char>::length(s1) ==
            std::char_traits<char>::length(s2) &&
            std::char_traits<char>::compare(
                s1, s2, std::char_traits<char>::length(s1)) == 0;
    }



    //-------------------------------------------------------------------------
    // MD5 sum per RFC 1321
    struct md5 
    {
        md5& update(const std::uint8_t* data, size_t length);

        md5& update(auto&& t) requires (std::is_aggregate_v<std::remove_cvref_t<decltype(t)>> or std::is_fundamental_v<std::remove_cvref_t<decltype(t)>>)
        {
            return update(reinterpret_cast<const std::uint8_t*>(std::addressof(t)), sizeof(t));
        }

        md5& update(std::ranges::sized_range auto&& r) requires(not std::is_aggregate_v<std::remove_cvref_t<decltype(r)>>)
        {
            return update(reinterpret_cast<const std::uint8_t*>(std::addressof(*std::ranges::begin(r))), std::ranges::size(r));
        }

        md5& update(const char* str)
        {
            return update(std::string_view(str));
        }

        md5& finilize();
            
        std::array<std::uint8_t, 16> digest() const;

        std::string to_string() const;

    private:
        void transform(const uint8_t block[64]);
        static void encode(uint8_t* output, const uint32_t* input, size_t length);
        static void decode(uint32_t* output, const uint8_t* input, size_t length);

        static uint32_t F(uint32_t x, uint32_t y, uint32_t z) { return (x & y) | (~x & z); }
        static uint32_t G(uint32_t x, uint32_t y, uint32_t z) { return (x & z) | (y & ~z); }
        static uint32_t H(uint32_t x, uint32_t y, uint32_t z) { return x ^ y ^ z; }
        static uint32_t I(uint32_t x, uint32_t y, uint32_t z) { return y ^ (x | ~z); }

        static void FF(uint32_t& a, uint32_t b, uint32_t c, uint32_t d, uint32_t x, uint32_t s, uint32_t ac) {
            a += F(b, c, d) + x + ac;
            a = rotate_left(a, s) + b;
        }
        static void GG(uint32_t& a, uint32_t b, uint32_t c, uint32_t d, uint32_t x, uint32_t s, uint32_t ac) {
            a += G(b, c, d) + x + ac;
            a = rotate_left(a, s) + b;
        }
        static void HH(uint32_t& a, uint32_t b, uint32_t c, uint32_t d, uint32_t x, uint32_t s, uint32_t ac) {
            a += H(b, c, d) + x + ac;
            a = rotate_left(a, s) + b;
        }
        static void II(uint32_t& a, uint32_t b, uint32_t c, uint32_t d, uint32_t x, uint32_t s, uint32_t ac) {
            a += I(b, c, d) + x + ac;
            a = rotate_left(a, s) + b;
        }

        static uint32_t rotate_left(uint32_t x, uint32_t n) {
            return (x << n) | (x >> (32 - n));
        }

        static constexpr uint32_t S11 = 7;
        static constexpr uint32_t S12 = 12;
        static constexpr uint32_t S13 = 17;
        static constexpr uint32_t S14 = 22;
        static constexpr uint32_t S21 = 5;
        static constexpr uint32_t S22 = 9;
        static constexpr uint32_t S23 = 14;
        static constexpr uint32_t S24 = 20;
        static constexpr uint32_t S31 = 4;
        static constexpr uint32_t S32 = 11;
        static constexpr uint32_t S33 = 16;
        static constexpr uint32_t S34 = 23;
        static constexpr uint32_t S41 = 6;
        static constexpr uint32_t S42 = 10;
        static constexpr uint32_t S43 = 15;
        static constexpr uint32_t S44 = 21;

        uint32_t state[4]{ 0x67452301, 0xefcdab89, 0x98badcfe, 0x10325476 }; // State (ABCD)
        uint32_t bitCount[2]{}; // Number of bits, modulo 2^64 (low-order word first)
        uint8_t buffer[64]; // Input buffer
        bool finilized = false;
    };

    //-------------------------------------------------------------------------
    // md5 functions
    inline auto md5string(auto&& ...t) 
    {
        md5 _{};
        (_.update(std::forward<decltype(t)>(t)), ...);
        return _.finilize().to_string();
    }
    inline auto md5digest(auto&& ...t)
    {
        md5 _{};
        (_.update(std::forward<decltype(t)>(t)), ...);
        return _.finilize().digest();
    }
